import java.util.*;
import java.lang.*;
import java.io.*;

/* Name of the class has to be "Main" only if the class is public. */
class Counting_Words
{
	public static void main (String[] args) 
	{
		Scanner sc=new Scanner(System.in);
		int T=sc.nextInt();
		while(T>0){
		    int N=sc.nextInt();
		    int M=sc.nextInt();
		    System.out.println(N*M);
		    T--;
		    }
	}
}
